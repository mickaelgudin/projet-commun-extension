	static getStringInterpolationRegex(): RegExp {
		return /({)+( )*[a-zA-z0-9()]*(})+/;
	}

	static isWellFormedStringInterpolation(text: string): boolean {
		return /^[^{]*{{( )*[a-zA-z0-9()]+( )*}}[^}]*$/.test(text);
	}

